let dia =1;
switch(dia){
    case 0:
        console.log('Es domingo');
        break;
    case 1:
        console.log('Es lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miercoles');
        break;
    case 4:
        console.log('Jueves');
        break;
    case 5:
        console.log('Viernes')
        break;
    case 6:
        console.log('Jueves');
        break;
    default:
        console.log('El dia ingresado no es permitido');

}